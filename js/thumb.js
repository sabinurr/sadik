    let currentIndex = 0;
    const intervalTime = 5000;
    function showMember(index) {
      currentIndex = index;
      const member = team[index];
      const img = document.getElementById("main-img");
      const name = document.getElementById("main-name");
      const title = document.getElementById("main-title");
      const desc = document.getElementById("main-desc");
      [img, name, title, desc].forEach(el => {
        el.classList.remove("animated");
        void el.offsetWidth; 
      });
      img.src = member.image;
      name.textContent = member.name;
      title.textContent = member.title;
      desc.textContent = member.desc;
      [img, name, title, desc].forEach(el => {
        el.classList.add("animated");
      });
    }
    let autoSwitch = setInterval(() => {
      currentIndex = (currentIndex + 1) % team.length;
      showMember(currentIndex);
    }, intervalTime);
    function createThumbnails() {
      const container = document.getElementById("thumbs");
      container.innerHTML = "";
      team.forEach((member, index) => {
        const div = document.createElement("div");
        div.className = "thumb";
        div.innerHTML = `<img src="${member.image}" alt="${member.name}" />
                         <span>${member.name}</span>`;
        div.onclick = () => {
          clearInterval(autoSwitch);
          showMember(index);
          autoSwitch = setInterval(() => {
            currentIndex = (currentIndex + 1) % team.length;
            showMember(currentIndex);
          }, intervalTime);
        };
        container.appendChild(div);
      });
    }
    showMember(0);
    createThumbnails();
 